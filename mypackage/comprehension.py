"""1. List comprehension

–ó–∞–¥–∞—á–∞:
–ü–æ–ª—É—á–∏—Ç—å —Å–ø–∏—Å–æ–∫ –∫–≤–∞–¥—Ä–∞—Ç–æ–≤ –Ω–µ—á—ë—Ç–Ω—ã—Ö —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ 10.

üëâ –ù–∞–ø–∏—à–∏ –æ–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–æ–π —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º for."""
print([n ** 2 for n in range(1, 11) if n % 2 != 0])  # List comprehension

"""2. Set comprehension

–ó–∞–¥–∞—á–∞:
–ü–æ–ª—É—á–∏—Ç—å –º–Ω–æ–∂–µ—Å—Ç–≤–æ –ø–µ—Ä–≤—ã—Ö –±—É–∫–≤ –≤—Å–µ—Ö —Å–ª–æ–≤ –≤ —Å–ø–∏—Å–∫–µ ["apple", "banana", "pear", "apricot", "blueberry"].

üëâ –ò—Å–ø–æ–ª—å–∑—É–π {... for ...}"""
print({l[0] for l in ["apple", "banana", "pear", "apricot", "blueberry", ""] if len(l) > 0})

"""3. Generator expression

–ó–∞–¥–∞—á–∞:
–ü–æ—Å—á–∏—Ç–∞—Ç—å —Å—É–º–º—É –≤—Å–µ—Ö —Ç—Ä—ë—Ö–∑–Ω–∞—á–Ω—ã—Ö —á–∏—Å–µ–ª, –¥–µ–ª—è—â–∏—Ö—Å—è –Ω–∞ 7.

üëâ –ò—Å–ø–æ–ª—å–∑—É–π sum(...) –∏ (for ...)"""
print((sum((n for n in range(100, 999 + 1) if n % 7 == 0))))

"""
4. –í–ª–æ–∂–µ–Ω–Ω—ã–µ list comprehensions

–ó–∞–¥–∞—á–∞:
–ü–æ–ª—É—á–∏—Ç—å —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö –ø–∞—Ä (x, y), –≥–¥–µ x –æ—Ç 1 –¥–æ 3, y –æ—Ç 1 –¥–æ 2.

üëâ –†–µ–∑—É–ª—å—Ç–∞—Ç: [(1,1), (1,2), (2,1), (2,2), (3,1), (3,2)]"""

print(  [(x,y) for x in range(1, 3 + 1) for y in range(1, 2 + 1)]  )


"""5. Set comprehension —Å —Ñ–∏–ª—å—Ç—Ä–∞—Ü–∏–µ–π

–ó–∞–¥–∞—á–∞:
–ü–æ–ª—É—á–∏—Ç—å –º–Ω–æ–∂–µ—Å—Ç–≤–æ —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö —Ü–∏—Ñ—Ä –∏–∑ —Å–ø–∏—Å–∫–∞ —Å—Ç—Ä–æ–∫:
["a1b2", "c3", "44", "b5a7"]

üëâ –¢–æ–ª—å–∫–æ —Ü–∏—Ñ—Ä—ã, –±–µ–∑ –ø–æ–≤—Ç–æ—Ä–æ–≤."""


print({n for string in ["a1b2", "c3", "44", "b5a7"] for n in string if n.isdecimal()})